# autopwn-framework/plugins/sample_plugin.py
"""
Пример простого плагина для autopwn-framework.

Содержит стандартные методы:
- init_plugin() — инициализация плагина
- activate() — активация функционала
- deactivate() — деактивация функционала

Плагин может расширять функционал фреймворка.
"""

def init_plugin():
    """
    Инициализация плагина при загрузке менеджером.
    Можно настроить необходимые ресурсы, переменные.
    """
    print("[sample_plugin] Инициализация плагина")

def activate():
    """
    Включение плагина в работу.
    Здесь обычно запускается основной функционал.
    """
    print("[sample_plugin] Плагин активирован")

def deactivate():
    """
    Остановка плагина.
    Освобождение ресурсов, остановка фоновых процессов.
    """
    print("[sample_plugin] Плагин деактивирован")
