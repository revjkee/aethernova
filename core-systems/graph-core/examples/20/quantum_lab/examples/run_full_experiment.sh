#!/bin/bash
# quantum-lab/examples/run_full_experiment.sh
#
# Скрипт для запуска полного эксперимента квантового алгоритма
# Включает подготовку окружения, запуск алгоритмов и сохранение результатов.
#
# Используется для автоматизации серии тестов и экспериментов с квантовыми алгоритмами
# в рамках проекта quantum-lab.

set -e

echo "Запуск полного эксперимента quantum-lab..."

# Активируем виртуальное окружение (предполагается, что оно настроено)
if [ -d "venv" ]; then
  source venv/bin/activate
  echo "Виртуальное окружение активировано"
else
  echo "Виртуальное окружение не найдено, пропуск активации"
fi

# Установка зависимостей (опционально)
echo "Установка зависимостей..."
pip install -r requirements.txt

# Запуск основных примеров по очереди
echo "Запуск simple_vqe.py..."
python examples/simple_vqe.py

echo "Запуск qaoa_chemistry.py..."
python examples/qaoa_chemistry.py

# Здесь можно добавить другие экспериментальные скрипты по мере необходимости

echo "Эксперимент завершён успешно."
